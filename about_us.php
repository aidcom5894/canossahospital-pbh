		                    <p class="mb-3" style="color:black;">Canossa Hospital generally known as <b>‘Mother Hospital’ was born out of a compelling need of alleviating the sufferings of sick people of the locality who were helpless and poor. It is named after St. Magdalene of Canossa (1774-1835), the Foundress of the Institutes of ‘Canossian Daughters of Charity’ and ‘Canossian Sons of Charity’</b>. She was a marchioness from Verona – Italy. It was out of profound love for God and a great sensitivity to the suffering people, Magdalene left the Canossa palace forever to educate the street children and assist and comfort the sick in the hospital. Love of God and love of neighbour was the solefoundation on which Magdalene of Canossa built her whole life. It follows that the care of the sick, helping the suffering brothers and sisters, could not be missing as she had a natural grace to instruct, comfort and assist the sick in the hospital and at home. Following the Spirit and Charism of St. Magdalene of Canossa, the Canossian sisters in Partapgarh started a small Health Center and Mobile Clinic in 1938 under the guidance of M.Rosetta Vigano who was a doctor. She treated them with simple home remedies coupled with love and grace of God which cured them. M. Ferrario who assisted her, once observed that ‘the people would come to the sistersrather than go to the doctors or to the government hospitals. Each day the number of patients kept increasing. Early morning even before the sisters had finished their morning prayers, people would be there in the convent premises for medicine and the influx would continue till late evening’.</p>

